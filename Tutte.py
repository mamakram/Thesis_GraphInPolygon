def longest_list(l,max_size=float('inf')):
	res = l[0]
	for elem in l:
		if len(elem)>len(res) and len(elem)<=max_size:
			res = elem
	return res
		


"""
Tutte embedding: Start with a 3-connected graph G with k vertices and an n-size Polygon P where a cycle of G of size n
has preset position. Place inner vertices st all faces are convex and planar and under tension wrt edge weights
Input:
@graph: the graph as a numpy matrix representing adjacency matrix
@p: Indices of the outer cycle of the graph
@pos: position of all points (initialised to (0,0) for inner vertices) as list of tuples

@output: A mapping of all vertices of graph following Tutte embeding
"""
def tutte_embedding(adj,p,pos,measure = False):
	#pos = p+[(0,0)]*(len(adj)-len(p)) #a list of node positions
	#map the inner vertices (n+1,...,k)
	size = len(adj)-len(p)
	#create 2 systems of equations to solve the weight constraints on inner vertices for both axes
	#x axis
	a = np.zeros((size,size))
	b = [0]*size
	#y axis
	c = np.zeros((size,size))
	d = [0]*size
	inner_vertices = [i for i in range(len(adj)) if i not in p]
	
	for i in range(len(adj)-len(p)):
		index = inner_vertices[i] #actual index in graph
		neighbors = [i for i in range(len(adj[index])) if adj[index][i]]
		n = len(neighbors)
		a[i][i] = 1 #weights
		c[i][i] = 1 #weights
		
		
		for v in neighbors:
			if v not in inner_vertices: #outer face vertices
				b[i] += float(pos[v][0])/n
				d[i] += float(pos[v][1])/n
			else: #inner vertices
				j = inner_vertices.index(v)
				a[i][j] = -(1/float(n))
				c[i][j] = -(1/float(n))
	#solve both equations
	t1= time.time()
	x = np.linalg.solve(c, b)
	y = np.linalg.solve(c, d)
	tfinal = time.time()-t1
	for i in range(len(adj)-len(p)):
		pos[inner_vertices[i]] = (x[i],y[i])
	if measure:
		return pos,tfinal
	return pos


